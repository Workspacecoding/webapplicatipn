const 公司類型 = document.getElementById("公司類型");
const 薪資級距 = document.getElementById("薪資級距");
const 負債比 = document.getElementById("負債比");
const 顯示利率區域 = document.getElementById("顯示利率");

公司類型.addEventListener("change", () => 更新利率());
薪資級距.addEventListener("change", () => 更新利率());
負債比.addEventListener("change", () => 更新利率());

function 更新利率() {
    const 公司類型值 = 公司類型.value;
    const 薪資級距值 = 薪資級距.value;
    const 負債比值 = 負債比.value;

    if (公司類型值 && 薪資級距值 && 負債比值) {
        const 利率 = 利率信息[公司類型值][薪資級距值][負債比值];
        顯示利率區域.textContent = 利率 + "%";
    } else {
        顯示利率區域.textContent = "請選擇";
    }
}